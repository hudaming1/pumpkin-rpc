package org.hum.pumpkin.config.spring.bean;

public class PumpkinProtocolBean {

	private String name;
	private int port;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getPort() {
		return port;
	}
	public void setPort(int port) {
		this.port = port;
	}
}
